    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblWorkRequests.getModel();

        model.setRowCount(0);
        WorkQueue workQueue = business.getWorkqueue();
        MedicationWorkRequest re = new MedicationWorkRequest();
        for (WorkRequest request : workQueue.getWorkRequestList()) {
            Object[] row = new Object[2];
            String med = ((MedicationWorkRequest)request).getMedication();
            row[0] = med == null ? "Waiting" :med;
            row[1] = request.getStatus();
            model.addRow(row);
        }
    }
